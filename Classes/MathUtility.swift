import Foundation

public class MathUtility {
    
    public let pi = 3.142345
    
    public init() {}
    
    public func add(a:Int, b:Int) -> Int {
        return a + b
    }
    public func sub(a:Int, b:Int) -> Int {
        return a - b
    }
    public func mul(a:Int, b:Int) -> Int {
        return a * b
    }
    public func div(a:Int, b:Int) -> Int {
        return a / b
    }
}
